public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("짜장면!");
        // 화면에 문자 출력한다
        System.out.println("짜장면" + "!");
        // 2개 문자열 합쳐서 나온다
        String HelloWorld = "짜장면!";
        // 문자열을 HelloWorld 변수에 대입하고 HelloWorld 변수의 내용을 출력한다
        System.out.println(HelloWorld);
    }
}
// 결론은 위 세가지 방법 모두 똑같은 문자열이 출력된다